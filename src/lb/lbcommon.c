void lbCommonCross3D(Vec3f *a, Vec3f *b, Vec3f *out)
{
	out->x = a->y * b->z - a->z * b->y;
	out->y = a->z * b->x - a->x * b->z;
	out->z = a->x * b->y - a->y * b->x;
}
