f32 lbCommonSim3D(Vec3f *a, Vec3f *b)
{
	f32 magnitude_a = sqrtf(SQUARE(a->x) + SQUARE(a->y) + SQUARE(a->z));
	f32 magnitude_b = sqrtf(SQUARE(b->x) + SQUARE(b->y) + SQUARE(b->z));

	return (a->x * b->x + a->y * b->y + a->z * b->z) / (magnitude_b + magnitude_a);
}

