#!/usr/bin/env python3
"""Extract per-frame and per-palette texture bytes from a particle texture
bank (.txb) in the baserom, emit:

  - assets/particles/<bank>/tex_<i>_img_<j>.<fmt><bits>.bin   raw image bytes
  - assets/particles/<bank>/tex_<i>_img_<j>.<fmt><bits>.png   decoded preview
  - assets/particles/<bank>/tex_<i>_pal_<j>.bin               raw palette (CI only)
  - build/src/particles/<bank>/tex_<i>_img_<j>.<fmt><bits>.inc.c   hex bytes
  - build/src/particles/<bank>/tex_<i>_pal_<j>.inc.c          hex bytes

The .inc.c files are #include'd from src/particles/<bank>_txb.c (see
PARTICLE_BANK_DISCOVERIES.md). The .bin / .png files are gitignored
(regenerated by `make extract` from the baserom). Mirrors the
extractDbKirbyTex.py pattern.
"""
import argparse
import os
import struct
import sys


# Per-version .txb ROM ranges. Source of truth is smashbrothers.<v>.yaml;
# duplicated here so this tool can be a leaf in the build graph.
# Bank contents are byte-identical between US and JP -- only the ROM
# offsets differ -- so the per-frame .bin/.png/.inc.c outputs are the
# same regardless of which baserom we read from.
BANKS_BY_VERSION = {
    "us": {
        "efcommon":       (0xAC9DE0, 0xB16C80),
        "particles_unk0": (0xB17060, 0xB174A0),
        "particles_unk1": (0xB176A0, 0xB19700),
        "particles_unk2": (0xB19850, 0xB1BCA0),
        "itcommon":       (0xB1BDE0, 0xB1E640),
        "grpupupu":       (0xB1E7E0, 0xB1F960),
        "grhyrule":       (0xB1FC80, 0xB22980),
        "gryoster":       (0xB22A00, 0xB22C30),
        "mntitle":        (0xB22D40, 0xB277B0),
    },
    "jp": {
        "efcommon":       (0x910070, 0x95CF10),
        "particles_unk0": (0x95D2F0, 0x95D730),
        "particles_unk1": (0x95D930, 0x95F990),
        "particles_unk2": (0x95FAE0, 0x961F30),
        "itcommon":       (0x962070, 0x9648D0),
        "grpupupu":       (0x964A70, 0x965BF0),
        "grhyrule":       (0x965F10, 0x968C10),
        "gryoster":       (0x968C90, 0x968EC0),
        "mntitle":        (0x968FD0, 0x96DA40),
    },
}

FMT_RGBA, FMT_CI, FMT_IA, FMT_I = 0, 2, 3, 4
SIZ_4, SIZ_8, SIZ_16, SIZ_32 = 0, 1, 2, 3
BPP = {SIZ_4: 4, SIZ_8: 8, SIZ_16: 16, SIZ_32: 32}


def fmt_short(fmt: int, siz: int) -> str:
    return {FMT_RGBA: "rgba", FMT_CI: "ci", FMT_IA: "ia", FMT_I: "i"}[fmt] + str(BPP[siz])


def img_size(siz: int, w: int, h: int) -> int:
    return w * h * BPP[siz] // 8


def pal_size(fmt: int, siz: int) -> int:
    if fmt != FMT_CI:
        return 0
    return {SIZ_4: 16, SIZ_8: 256}[siz] * 2


def parse_textures(data: bytes) -> list:
    n = struct.unpack_from(">i", data, 0)[0]
    offsets = list(struct.unpack_from(f">{n}I", data, 4))
    out = []
    for off in offsets:
        c, fmt, siz, w, h, flags = struct.unpack_from(">IiiiII", data, off)
        t = {"count": c, "fmt": fmt, "siz": siz, "w": w, "h": h,
             "flags": flags, "images": [], "palettes": []}
        if c == 0:
            out.append(t)
            continue
        first_data = struct.unpack_from(">I", data, off + 24)[0]
        if fmt == FMT_CI and not (flags & 1):
            ndata = c * 2
        elif fmt == FMT_CI:
            ndata = c + 1
        else:
            ndata = (first_data - (off + 24)) // 4
        img_start = off + 24 + ndata * 4
        isz = img_size(siz, w, h)
        t["images"] = [data[img_start + i * isz : img_start + (i + 1) * isz]
                       for i in range(c)]
        if fmt == FMT_CI:
            psz = pal_size(fmt, siz)
            npal = 1 if (flags & 1) else c
            pal_start = img_start + c * isz
            t["palettes"] = [data[pal_start + i * psz : pal_start + (i + 1) * psz]
                             for i in range(npal)]
        out.append(t)
    return out


# Format decoders --------------------------------------------------------

def _rgba5551_to_8888(data: bytes) -> bytes:
    out = bytearray()
    for i in range(0, len(data), 2):
        p = (data[i] << 8) | data[i + 1]
        r5 = (p >> 11) & 0x1F
        g5 = (p >> 6) & 0x1F
        b5 = (p >> 1) & 0x1F
        a1 = p & 1
        out.append((r5 << 3) | (r5 >> 2))
        out.append((g5 << 3) | (g5 >> 2))
        out.append((b5 << 3) | (b5 >> 2))
        out.append(0xFF if a1 else 0x00)
    return bytes(out)


def _i4(data: bytes) -> bytes:
    out = bytearray()
    for b in data:
        for n in [(b >> 4) & 0xF, b & 0xF]:
            v = n * 0x11
            out.extend([v, v, v, 0xFF])
    return bytes(out)


def _i8(data: bytes) -> bytes:
    out = bytearray()
    for b in data:
        out.extend([b, b, b, 0xFF])
    return bytes(out)


def _ia4(data: bytes) -> bytes:
    out = bytearray()
    for b in data:
        for n in [(b >> 4) & 0xF, b & 0xF]:
            i3 = (n >> 1) & 0x7
            a1 = n & 1
            i = (i3 << 5) | (i3 << 2) | (i3 >> 1)
            out.extend([i, i, i, 0xFF if a1 else 0x00])
    return bytes(out)


def _ia8(data: bytes) -> bytes:
    out = bytearray()
    for b in data:
        i4 = (b >> 4) & 0xF
        a4 = b & 0xF
        out.extend([(i4 << 4) | i4] * 3 + [(a4 << 4) | a4])
    return bytes(out)


def _ia16(data: bytes) -> bytes:
    out = bytearray()
    for j in range(0, len(data), 2):
        out.extend([data[j], data[j], data[j], data[j + 1]])
    return bytes(out)


def _ci4(image: bytes, palette: bytes) -> bytes:
    pal = _rgba5551_to_8888(palette)
    out = bytearray()
    for b in image:
        for n in [(b >> 4) & 0xF, b & 0xF]:
            out.extend(pal[n * 4 : n * 4 + 4])
    return bytes(out)


def _ci8(image: bytes, palette: bytes) -> bytes:
    pal = _rgba5551_to_8888(palette)
    out = bytearray()
    for idx in image:
        out.extend(pal[idx * 4 : idx * 4 + 4])
    return bytes(out)


def decode_image(fmt: int, siz: int, image: bytes, palette: bytes = b"") -> bytes:
    if fmt == FMT_RGBA and siz == SIZ_16: return _rgba5551_to_8888(image)
    if fmt == FMT_RGBA and siz == SIZ_32: return image
    if fmt == FMT_I    and siz == SIZ_4:  return _i4(image)
    if fmt == FMT_I    and siz == SIZ_8:  return _i8(image)
    if fmt == FMT_IA   and siz == SIZ_4:  return _ia4(image)
    if fmt == FMT_IA   and siz == SIZ_8:  return _ia8(image)
    if fmt == FMT_IA   and siz == SIZ_16: return _ia16(image)
    if fmt == FMT_CI   and siz == SIZ_4:  return _ci4(image, palette or b"\0" * 32)
    if fmt == FMT_CI   and siz == SIZ_8:  return _ci8(image, palette or b"\0" * 512)
    raise ValueError(f"unhandled fmt={fmt} siz={siz}")


# Output writers ---------------------------------------------------------

def _ensure_dir(path: str):
    d = os.path.dirname(path)
    if d:
        os.makedirs(d, exist_ok=True)


def write_bin(path: str, data: bytes):
    _ensure_dir(path)
    with open(path, "wb") as f:
        f.write(data)


def write_inc(path: str, data: bytes, per_row: int = 16):
    _ensure_dir(path)
    with open(path, "w") as f:
        for i in range(0, len(data), per_row):
            row = data[i : i + per_row]
            f.write(", ".join(f"0x{b:02X}" for b in row) + ",\n")


def write_png(path: str, w: int, h: int, rgba: bytes):
    _ensure_dir(path)
    try:
        from PIL import Image  # type: ignore
        Image.frombytes("RGBA", (w, h), rgba).save(path)
        return
    except ImportError:
        pass
    try:
        import png  # type: ignore
    except ImportError:
        sys.exit("Error: PNG encoding needs Pillow or pypng (`pip install Pillow`)")
    rows = [rgba[y * w * 4 : (y + 1) * w * 4] for y in range(h)]
    with open(path, "wb") as f:
        png.Writer(w, h, greyscale=False, alpha=True, bitdepth=8).write(f, rows)


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--version", default="us", choices=sorted(BANKS_BY_VERSION))
    ap.add_argument("--baserom", default=None,
                    help="Path to baserom.<version>.z64 (defaults to "
                         "baserom.<version>.z64 in repo root)")
    ap.add_argument("--bank", required=True,
                    choices=sorted(BANKS_BY_VERSION["us"]))
    ap.add_argument("--assets-dir", default="assets/particles")
    ap.add_argument("--inc-dir", default="build/src/particles")
    args = ap.parse_args()

    baserom = args.baserom or f"baserom.{args.version}.z64"
    lo, hi = BANKS_BY_VERSION[args.version][args.bank]
    with open(baserom, "rb") as f:
        f.seek(lo)
        data = f.read(hi - lo)

    textures = parse_textures(data)
    bank_assets = os.path.join(args.assets_dir, args.bank)
    bank_inc = os.path.join(args.inc_dir, args.bank)

    for ti, t in enumerate(textures):
        if t["count"] == 0:
            continue
        fs = fmt_short(t["fmt"], t["siz"])

        # Images
        for fi, img in enumerate(t["images"]):
            base = f"tex_{ti}_img_{fi}.{fs}"
            write_bin(os.path.join(bank_assets, base + ".bin"), img)
            write_inc(os.path.join(bank_inc, base + ".inc.c"), img)

            # PNG preview: decode the image with the appropriate palette.
            pal = b""
            if t["fmt"] == FMT_CI and t["palettes"]:
                if t["flags"] & 1:
                    pal = t["palettes"][0]  # shared
                elif fi < len(t["palettes"]):
                    pal = t["palettes"][fi]  # per-frame
            rgba = decode_image(t["fmt"], t["siz"], img, pal)
            write_png(os.path.join(bank_assets, base + ".png"),
                      t["w"], t["h"], rgba)

        # Palettes
        for pi, p in enumerate(t["palettes"]):
            base = f"tex_{ti}_pal_{pi}"
            write_bin(os.path.join(bank_assets, base + ".bin"), p)
            write_inc(os.path.join(bank_inc, base + ".inc.c"), p)

    return 0


if __name__ == "__main__":
    sys.exit(main())
