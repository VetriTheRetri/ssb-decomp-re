#!/usr/bin/env python3
"""
Restructure Fighter Main relocData files to use:
  1. A single contiguous u32 pre-attributes block
  2. A typed FTAttributes struct (via typeFTAttributes.py)
  3. Optional trailing padding
  4. A clean .reloc file with symbolic labels

This replaces the fragmented ptrs/script/pre arrays generated by promoteMotionFiles.py.
"""

import struct
import sys
import os
import csv

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_DIR = os.path.join(SCRIPT_DIR, "..")

# Import from typeFTAttributes.py
sys.path.insert(0, SCRIPT_DIR)
from typeFTAttributes import (
    FTATTR_SIZE, read_bin, ru32, parse_ftattributes, generate_attr_block
)

# Fighter Main files: (file_id, name, o_attributes)
FIGHTER_MAINS = [
    (203, "MarioMain",     0x0428),
    (206, "MMarioMain",    0x02A8),
    (207, "NMarioMain",    0x0298),
    (209, "FoxMain",       0x046C),
    (211, "NFoxMain",      0x02A4),
    (213, "DonkeyMain",    0x04A4),
    (214, "NDonkeyMain",   0x0298),
    (215, "GDonkeyMain",   0x03C8),
    (219, "NSamusMain",    0x03BC),
    (221, "LuigiMain",     0x0580),
    (223, "NLuigiMain",    0x0298),
    (227, "NLinkMain",     0x02D8),
    (231, "NKirbyMain",    0x02C0),
    (233, "PurinMain",     0x0474),
    (234, "NPurinMain",    0x02A0),
    (236, "CaptainMain",   0x0488),
    (237, "NCaptainMain",  0x029C),
    (241, "NNessMain",     0x02F0),
    (245, "NPikachuMain",  0x02A8),
    (248, "NYoshiMain",    0x02B8),
    (250, "BossMain",      0x00E8),
]


def walk_chain(data, start_word):
    """Walk a reloc chain. Returns list of (ptr_byte_offset, target_byte_offset)."""
    entries = []
    cur = start_word
    while cur != 0xFFFF:
        off = cur * 4
        if off + 4 > len(data):
            break
        val = struct.unpack('>I', data[off:off+4])[0]
        next_word = (val >> 16) & 0xFFFF
        target_word = val & 0xFFFF
        entries.append((off, target_word * 4))
        cur = next_word
    return entries


def get_csv_reloc_info(fid):
    """Get intern/extern chain start words from CSV."""
    csv_path = os.path.join(PROJECT_DIR, "assets", "us", "relocData.csv")
    with open(csv_path) as f:
        reader = csv.reader(f)
        header = next(reader)
        for i, row in enumerate(reader):
            if i == fid:
                intern_start = int(row[2].strip(), 16)
                extern_start = int(row[4].strip(), 16)
                return intern_start, extern_start
    raise ValueError(f"File ID {fid} not found in CSV")


def generate_c_file(fid, name, o_attributes):
    """Generate the .c file content."""
    data = read_bin(fid)
    file_size = len(data)
    attr_end = o_attributes + FTATTR_SIZE

    # Get reloc chains
    intern_start, extern_start = get_csv_reloc_info(fid)
    intern_entries = walk_chain(data, intern_start)
    extern_entries = walk_chain(data, extern_start)

    # Build sets of ptr offsets for quick lookup
    intern_map = {ptr: target for ptr, target in intern_entries}
    extern_map = {ptr: target for ptr, target in extern_entries}

    # Number of pre-attr words
    pre_words = o_attributes // 4

    lines = []
    lines.append(f"/* Fighter Main data for relocData file {fid} ({name}) */")
    lines.append(f"/* File size: {file_size} bytes (0x{file_size:X}) */")
    lines.append("")
    lines.append('#include "relocdata_types.h"')
    lines.append("#include <ft/fttypes.h>")
    lines.append("")

    # Pre-attributes block
    lines.append(f"/* Pre-attributes data ({pre_words} words, 0x{o_attributes:04X} bytes) */")
    lines.append(f"u32 d{name}_pre[{pre_words}] = {{")

    # Emit pre-attr words, 1 per line with reloc comments
    for i in range(pre_words):
        byte_off = i * 4
        val = ru32(data, byte_off)
        hex_val = f"0x{val:08X}"

        comment = ""
        if byte_off in intern_map:
            target = intern_map[byte_off]
            comment = f" /* intern -> 0x{target:04X} */"
        elif byte_off in extern_map:
            target = extern_map[byte_off]
            comment = f" /* extern -> 0x{target:04X} */"

        lines.append(f"\t{hex_val},{comment}")

    lines.append("};")
    lines.append("")

    # FTAttributes block
    attr_fields = parse_ftattributes(data, o_attributes)
    attr_block = generate_attr_block(name, attr_fields)
    lines.append(attr_block)

    # Trailing padding
    if file_size > attr_end:
        trailing_bytes = file_size - attr_end
        trailing_words = trailing_bytes // 4
        if trailing_words > 0:
            lines.append("")
            lines.append(f"/* Trailing padding ({trailing_words} words) */")
            lines.append(f"u32 d{name}_pad[{trailing_words}] = {{")
            pad_vals = []
            for i in range(trailing_words):
                val = ru32(data, attr_end + i * 4)
                pad_vals.append(f"0x{val:08X}")
            lines.append("\t" + ", ".join(pad_vals) + ",")
            lines.append("};")

    lines.append("")
    return "\n".join(lines)


def generate_reloc_file(fid, name, o_attributes):
    """Generate the .reloc file content."""
    data = read_bin(fid)
    file_size = len(data)
    attr_end = o_attributes + FTATTR_SIZE

    # Get reloc chains
    intern_start, extern_start = get_csv_reloc_info(fid)
    intern_entries = walk_chain(data, intern_start)
    extern_entries = walk_chain(data, extern_start)

    lines = []
    lines.append(f"# Relocation metadata for file {fid}")
    lines.append("# Format: <type> <ptr_label> <target_label>")
    lines.append("# Labels are C variable names resolved via .o symbol table")

    # Combine and sort all entries by ptr offset for clean output
    all_entries = []
    for ptr, target in intern_entries:
        all_entries.append(("intern", ptr, target))
    for ptr, target in extern_entries:
        all_entries.append(("extern", ptr, target))
    all_entries.sort(key=lambda x: x[1])  # sort by ptr offset

    for chain_type, ptr_off, target_off in all_entries:
        # Determine ptr label
        if ptr_off < o_attributes:
            ptr_label = f"d{name}_pre+0x{ptr_off:X}"
        elif ptr_off < attr_end:
            attr_rel = ptr_off - o_attributes
            ptr_label = f"d{name}_attr+0x{attr_rel:X}"
        else:
            pad_rel = ptr_off - attr_end
            ptr_label = f"d{name}_pad+0x{pad_rel:X}"

        # Determine target label
        if chain_type == "extern":
            target_label = f"0x{target_off:X}"
        else:
            # intern: target is within the file
            if target_off < o_attributes:
                target_label = f"d{name}_pre+0x{target_off:X}"
            elif target_off < attr_end:
                attr_rel = target_off - o_attributes
                target_label = f"d{name}_attr+0x{attr_rel:X}"
            else:
                pad_rel = target_off - attr_end
                target_label = f"d{name}_pad+0x{pad_rel:X}"

        lines.append(f"{chain_type} {ptr_label} {target_label}")

    lines.append("")
    return "\n".join(lines)


def process_fighter(fid, name, o_attributes):
    """Process a single fighter main file."""
    print(f"Processing {fid}_{name} (attr at 0x{o_attributes:04X})...")

    c_content = generate_c_file(fid, name, o_attributes)
    reloc_content = generate_reloc_file(fid, name, o_attributes)

    c_path = os.path.join(PROJECT_DIR, "src", "relocData", f"{fid}_{name}.c")
    reloc_path = os.path.join(PROJECT_DIR, "src", "relocData", f"{fid}_{name}.reloc")

    with open(c_path, 'w') as f:
        f.write(c_content)

    with open(reloc_path, 'w') as f:
        f.write(reloc_content)

    print(f"  Wrote {c_path}")
    print(f"  Wrote {reloc_path}")
    return True


def main():
    if len(sys.argv) > 1:
        targets = []
        for arg in sys.argv[1:]:
            fid = int(arg)
            for f in FIGHTER_MAINS:
                if f[0] == fid:
                    targets.append(f)
                    break
            else:
                print(f"Unknown file ID: {fid}")
                return 1
    else:
        targets = FIGHTER_MAINS

    for fid, name, o_attr in targets:
        if not process_fighter(fid, name, o_attr):
            print(f"  FAILED!")
            return 1

    print("\nDone!")
    return 0


if __name__ == "__main__":
    sys.exit(main())
